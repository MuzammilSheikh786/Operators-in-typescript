//Logical Operator :
// {AND(&&),OR(||),NOT(!)/inverter}

// && 
let num1 = 20
let num2 = 10
console.log(num1!=num2 && num1>num2);
//||
let num3 = 15
let num4 = 20
console.log(num3==num4 || num3<num4);

// NOT(!)/inverter :
let num6 = 15
let num8 = 15
console.log(!(num6<num8));


// Arithmetic operator:
// + , - ,* , /
